#include "ft_printf.h"

int	ft_putnbar(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		write (1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		n = -n;
		i++;
		write (1, "-", 1);
	}
	if (n > 9)
		i += ft_putnbar(n / 10);
	i += ft_putchar(n % 10 + '0');
	return (i);
}
